def efetch_fasta(ids: list[int], label: str, out_path: Path) -> int:
    chunk_size = 100
    count = 0
    with open(out_path, "w") as f:
        for i in range(0, len(ids), chunk_size):
            chunk = ids[i:i + chunk_size]
            id_str = ",".join(str(x) for x in chunk)
            r = requests.post(f"{BASE}/efetch.fcgi", data={
                "db": "protein", "id": id_str,
                "rettype": "fasta", "retmode": "text", "email": EMAIL,
            }, timeout=60)
            r.raise_for_status()
            f.write(r.text)
            count += r.text.count(">")
            print(f"  [{label}] {i + len(chunk)}/{len(ids)}")
            time.sleep(0.4)
    return count


